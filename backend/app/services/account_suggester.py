"""自動仕訳ルール — 摘要からの相手科目推測 (M2 F-105)。

摘要のキーワードから相手科目コードを推測する。CSV 明細取込 (M5) の科目候補に
用いる。初期実装は簡易なキーワード辞書 (将来は学習ベースに拡張可能、全体§9)。

由来: 要件定義 F-105 / cash-bank 基本設計 §7 (科目推測の結合点)。
"""

# (キーワード群, 推測する勘定科目コード)。上から順にマッチを評価する。
KEYWORD_RULES: list[tuple[tuple[str, ...], str]] = [
    (("売上", "ご入金", "報酬", "請求"), "500"),  # 売上高
    (("振込手数料", "手数料", "atm"), "613"),  # 支払手数料
    (("電気", "ガス", "水道", "光熱", "電力"), "602"),  # 水道光熱費
    (("電話", "携帯", "通信", "インターネット", "プロバイダ", "wifi"), "604"),  # 通信費
    (("タクシー", "電車", "交通", "ガソリン", "高速", "etc", "鉄道", "バス"), "603"),  # 旅費交通費
    (("家賃", "地代", "賃料", "テナント"), "612"),  # 地代家賃
    (("保険",), "606"),  # 損害保険料
    (("外注", "業務委託"), "611"),  # 外注工賃
    (("接待", "会議", "飲食", "懇親"), "605"),  # 接待交際費
    (("amazon", "アマゾン", "文具", "消耗", "事務用品", "ヨドバシ"), "608"),  # 消耗品費
    (("修繕", "修理", "メンテナンス"), "607"),  # 修繕費
]


def suggest_account(description: str) -> str | None:
    """摘要から相手科目コードを推測する。

    Args:
        description: 取引の摘要 (CSV 明細の摘要列など)。

    Returns:
        推測した勘定科目コード。該当キーワードが無ければ None。
    """
    text = description.lower()
    for keywords, code in KEYWORD_RULES:
        if any(keyword.lower() in text for keyword in keywords):
            return code
    return None
